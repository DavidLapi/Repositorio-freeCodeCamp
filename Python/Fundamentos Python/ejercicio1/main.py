# Variable name
name = "Alice"
# Imprime name
print(name)
# Imprime tipo de dato de name
print(type(name))

# Variable is_student
is_student = True
# Imprime is_student y su tipo de dato
print(is_student, type(is_student))

# Imprime name y su tipo de dato
print(name, type(name))

# Variable age
age = 20
# Imprime age y su tipo de dato
print(age, type(age))

# Variable score
score = 80.5
# Verificar si score es un número entero (int) con la función 'isinstance'
print(isinstance(score, int))
# Verificar si score es un número con decimales (float) con la función 'isinstance'
print(isinstance(score, float))
# Imprime score y su tipo de dato
print(score, type(score))